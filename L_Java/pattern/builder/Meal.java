package builder;

/**
 * 套餐类
 * @author 2018/03/31
 *
 */
public class Meal {

	// food和drink是部件
	private String food;
	private String drink;
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	public String getDrink() {
		return drink;
	}
	public void setDrink(String drink) {
		this.drink = drink;
	}
	
}
